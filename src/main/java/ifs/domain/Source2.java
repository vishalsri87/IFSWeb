package ifs.domain;

public class Source2 {
	private String empId;
	private String name;

	public Source2(String empId, String name) {
		this.empId = empId;
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmpId() {
		return empId;
	}

	public void setEmpId(String empId) {
		this.empId = empId;
	}

}
